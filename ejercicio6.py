# menor o igual que
print(3 <= 2.9)                     #false
print(3 <= 2.99999999999999999)     #true   
print(45 <= 45)                     #true 
print([3, 4, 89] <= [3, 5, 87])     #true
print(False <= 0)                   #true 
print("Vamos" <= "vamos")           #true 
print(345.23 <= 4)                  #false 
print(345.23 <= "4")                #error message